namespace Patterns
{
	template <typename T> class Singleton : public Noncopyable
	{
		public:
		static T& GetInstance()
		{
			static T instance;
			return instance;
		}

		protected:
		Singleton() = default;
		virtual ~Singleton() = default;
	};
} // namespace Patterns
