void __cdecl CLog::Print(LPCSTR format, ...)
{
	va_list mark;
	string1024 buf;
	va_start(mark, format);
	int sz = _vsnprintf_s(buf, sizeof(buf) - 1, format, mark);
	buf[sizeof(buf) - 1] = 0;
	va_end(mark);
	if (sz)
	{
		std::cout << (buf);
		std::cout << "\n";
	}
}
